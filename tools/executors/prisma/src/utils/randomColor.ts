import chalk from 'chalk';

const COLORS = [
  'black',
  'red',
  'green',
  'yellow',
  'blue',
  'magenta',
  'cyan',
  'white',
  'gray',
  'grey',
  'blackBright',
  'redBright',
  'greenBright',
  'yellowBright',
  'blueBright',
  'magentaBright',
  'cyanBright',
  'whiteBright'
];

export const randomColor = () => {
  return chalk[COLORS[Math.floor(Math.random() * COLORS.length)]];
}
